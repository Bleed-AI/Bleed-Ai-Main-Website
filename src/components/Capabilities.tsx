function CapabilityCard({ capability, index }: { capability: typeof capabilities[0]; index: number }) {
  const ref = useRef<HTMLDivElement>(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            setIsVisible(true);
          }
        });
      },
      { threshold: 0.1, rootMargin: "0px 0px -50px 0px" }
    );

    if (ref.current) {
      observer.observe(ref.current);
    }

    return () => observer.disconnect();
  }, []);

  return (
    <div
      ref={ref}
      className={`group p-6 rounded-2xl border border-[var(--border-color)] bg-[var(--bg-secondary)] transition-all duration-500 hover:border-[#B1130F]/50 hover:shadow-[0_0_40px_rgba(177,19,15,0.1)] ${
        isVisible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-8"
      }`}
      style={{ transitionDelay: `${index * 50}ms` }}
    >
      {/* Icon */}
      <div className="w-12 h-12 rounded-xl bg-[#B1130F] flex items-center justify-center text-white mb-4 group-hover:scale-110 transition-transform duration-300">
        {capability.icon}
      </div>

      {/* Number */}
      <div className="text-xs font-medium text-[#B1130F] mb-2">{capability.number}</div>

      {/* Title */}
      <h3 className="font-display text-lg font-semibold text-[var(--text-primary)] mb-2">
        {capability.title}
      </h3>

      {/* Description */}
      <p className="text-sm text-[var(--text-muted)] leading-relaxed">
        {capability.description}
      </p>
    </div>
  );
}

